var cadena = "abc";
var curso = "Curso de Javascript 2016";

// Repetir cadena
console.log(cadena.repeat(5));

// Buscar si una cadena contiene un texto
console.log(curso.includes("Javascript"));

// Buscar si una cadena contiene un texto en cierto indice
