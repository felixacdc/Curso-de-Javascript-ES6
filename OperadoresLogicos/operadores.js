/*
OPERADOR        DESCRIPCION
    ==              Igual a
    !=              Direrente que
    <               Menor que
    <=              Menor o igual que
    >               Mayor que
    >=              Mayor o igual que
    &&              Operador and (y)
    ||              Operador or (o)
    !               Operador not (no)
*/

console.log(5 == 6);
console.log(5 != 6);
console.log(5 < 5);
console.log(5 <= 5);
console.log(5 > 6);
console.log(5 >= 6);

console.log(true && true);
console.log(true && false);
console.log(true || true);
console.log(true || false);
console.log(!false);